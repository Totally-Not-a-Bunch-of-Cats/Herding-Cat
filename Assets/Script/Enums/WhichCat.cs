/// <summary>
/// Selection types for the cats.
/// <list type="bullet">
///     <item>CURRENT - The current cat that is interacting with the item</item>
///     <item>CLOSEST - The closest cat to the item</item>
///     <item>ALL - Select all active cats</item>
/// </list>
/// </summary>
public enum WhichCat
{
    CURRENT,
    CLOSEST,
    ALL
}
